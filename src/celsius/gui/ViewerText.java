    private void findNext() {
        if (search.length()==0) return;
        int p=jTAContent.getText().indexOf(search,pos);
        if ((pos==0) && (p==-1)) {
            RSC.showWarning("Text not found.", "No result");
            return;
        }
        if (p==-1) {
            if (RSC.askQuestionYN("Text not found in remainder of the document.\nStart search from the top?", "Please confirm:")==JOptionPane.YES_OPTION) {
                pos=0;
                findNext();
            }
            return;
        }
        pos=p;
        jTAContent.requestFocus();
        jTAContent.setCaretPosition(pos);
        jTAContent.select(pos,pos+search.length());
        pos++;

    }

