    private void jMISaveCopyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMISaveCopyActionPerformed
        JFileChooser FC = new JFileChooser();
        FC.setDialogTitle("Indicate the target file");
        FC.setDialogType(JFileChooser.SAVE_DIALOG);
        FC.setFileFilter(new FFilter("_ALL", "All files"));
        RSC.adjustComponents(FC.getComponents());
        // abgebrochen?
        if (FC.showSaveDialog(this) != JFileChooser.CANCEL_OPTION) {
           (new InteractiveFileCopy(RSC.MF,filename, FC.getSelectedFile().getAbsolutePath(),RSC)).go();
        }
    }//GEN-LAST:event_jMISaveCopyActionPerformed

