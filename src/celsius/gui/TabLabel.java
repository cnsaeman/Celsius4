    public void actionPerformed(ActionEvent e) {
        if (((JButton)e.getSource()).getName().equals("switch")) {
            celsiusTable.switchView();
        }
        if (((JButton)e.getSource()).getName().equals("close")) {
            celsiusTable.close();
        }
    }

